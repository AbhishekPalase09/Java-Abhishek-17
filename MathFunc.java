public class MathFunc {
    public static void main(String[] args) {

        System.out.println(Math.abs(-10));   // Absolute value
        System.out.println(Math.sqrt(25));   // Square root
        System.out.println(Math.pow(2, 3));  // Power (2^3)
        System.out.println(Math.max(10, 20));// Maximum
        System.out.println(Math.min(10, 20));// Minimum

    }
}
